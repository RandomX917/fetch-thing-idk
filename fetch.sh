#!/usr/bin/env bash

cpu="$( grep 'model name' /proc/cpuinfo | uniq | cut -d: -f2)"
total="$(free -m | awk '/^Mem:/ {print $2}')"
used="$(free -m | awk '/^Mem:/ {print $3}')"
pct="$(( used * 100 / total ))"
if [ -z "$XDG_CURRENT_DESKTOP" ]; then
wmde=""
else
wmde="WM/DE:\033[0m $XDG_CURRENT_DESKTOP"
fi
if [ -f /etc/os-release ]; then
    . /etc/os-release
    os="$PRETTY_NAME"
else
    os="$(uname -s)"
fi
if [[ "$os"==NixOS* ]]; then
set -- $'\033[34m' $'\033[36m' $'\033[0m'
echo -e "\
$1        __   $2 ____    __
$1       /##\\   $2\\###\\  /##\\
$1       \\###\\   $2\\###\\/###/
$1     ___\\###\\___$2\\######/
$1    /############$2\\####/   $1/\\     $1 OS:$3 $os
$1   /##############$2\\###\\  $1/##\\    $2 Shell:$3 $(basename "$SHELL")
$1   ‾‾‾‾‾$2/###/$1‾‾‾‾‾ $2\\###\\$1/###/   $1  CPU:$3$cpu
$2 ______/###/        $2\\##$1/###/___  $2 Mem:$3 $used""MB / $total""MB ($pct%)
$2/#########/          $2\\$1/########\\ $1 $wmde
$2\\########/$1\\          $1/#########/
$2 ‾‾‾/###/$1##\\        $1/###/‾‾‾‾‾‾
$2   /###/$1\\###\\$2 _____$1/###/$2 ____
$2   \\##/  $1\\###\\$2##############/
$2    \\/   $1/####\\$2############/
$1        /######\\$2‾‾‾\\###\\‾‾‾
$1       /###/\\###\\$2   \\###\\
$1       \\##/  \\###\\$2   \\##/
$1        ‾‾    ‾‾‾‾$2    ‾‾
$3"
else
	set -- $'\033[0m' $'\033[0m' $'\033[0m'
echo -e "
$3 #####             #####
$3 #####     ###     #####
$3 ###     ######      ###
$3 ###    ###   ###    ###   OS: $os
$3 ###   ###     ###   ###   SHELL: $(basename "$SHELL")
$3 ###           ###   ###   CPU: $cpu
$3 ###           ###   ###   Mem $used""MB / $total""MB ($pct%)
$3 ###           ###   ###   $wmde
$3 ###          ###    ###
$3 ###         ###     ###
$3 ###        ###      ###
$3 ###       ###       ###
$3 ###       ###       ###
$3 ###       ###       ###
$3 ###                 ### 
$3 ###       ###       ###
$3 #####     ###     #####
$3 #####     ###     #####
"
fi
